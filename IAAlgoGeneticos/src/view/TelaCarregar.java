    private void btCarregarNodosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCarregarNodosActionPerformed
        // TODO add your handling code here:
        if (filechooser==null)
        {
            filechooser = new JFileChooser();
        }
        filechooser.setFileSelectionMode(JFileChooser.FILES_ONLY);
        boolean dispose = false;
        int returnVal = filechooser.showOpenDialog(this);
        Grafo grafo = null;
        if (returnVal == JFileChooser.APPROVE_OPTION)
        {
            File file = filechooser.getSelectedFile();
            grafo = Processar.processar(file);
            if (grafo!=null)
            {
                System.out.println("SUCESSO");
                MenuPrincipal novoMenu = new MenuPrincipal(grafo);
                novoMenu.setVisible(true);
                this.dispose();
                this.setVisible(false);
                dispose = true;
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Opa, parece que vocÃª selecionou o arquivo errado, tente de novo selecionar o arquivo 'a280.tsp'");
            }
        }
        else
        {
            
        }
        if (!dispose)
        {
            
        }
    }//GEN-LAST:event_btCarregarNodosActionPerformed

